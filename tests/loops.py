# ------------------------------ for loop --------------------------------------

nums = [1,2,3,4,5]

for n in nums:
	if n == 3:
		print('found!')
		break
	print(n)

for i in range(len(nums)):
	if nums[i] == 3:
		print('found!')
		continue	
	print(nums[i])


